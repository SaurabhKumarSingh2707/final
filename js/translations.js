  setupEventListeners() {
    // Use event delegation for dynamically created elements
    document.addEventListener('click', (e) => {
      if (e.target.closest('#languageSelector')) {
        e.stopPropagation();
        this.toggleLanguageDropdown();
      } else if (e.target.closest('.language-option')) {
        const lang = e.target.closest('.language-option').dataset.lang;
        this.changeLanguage(lang);
      } else if (!e.target.closest('.language-selector-nav')) {
        this.closeLanguageDropdown();
      }
    });
    
    document.addEventListener('keydown', (e) => {
      if (e.key === 'Escape') {
        this.closeLanguageDropdown();
      }
    });
  }
  
  toggleLanguageDropdown() {
    const dropdown = document.getElementById('languageDropdown');
    if (dropdown) {
      dropdown.classList.toggle('show');
    }
  }
  
  closeLanguageDropdown() {
    const dropdown = document.getElementById('languageDropdown');
    if (dropdown) {
      dropdown.classList.remove('show');
    }
  }
  
  changeLanguage(lang) {
    if (!this.translations[lang]) return;
    
    this.currentLanguage = lang;
    localStorage.setItem('agritech_language', lang);
    
    this.translatePage();
    this.updateLanguageDisplay();
    this.closeLanguageDropdown();
    
    // Update voice assistant language if available
    if (window.voiceAssistant && window.voiceAssistant.changeLanguage) {
      const voiceLanguageMap = {
        'en': 'en-US',
        'hi': 'hi-IN'
      };
      
      const voiceLang = voiceLanguageMap[lang] || 'en-US';
      window.voiceAssistant.changeLanguage(voiceLang);
    }
    
    // Trigger custom event for other components
    window.dispatchEvent(new CustomEvent('languageChanged', { 
      detail: { language: lang, translations: this.translations[lang] } 
    }));
  }
  
  translatePage() {
    const elements = document.querySelectorAll('[data-translate]');
    const translations = this.translations[this.currentLanguage];
    
    elements.forEach(element => {
      element.classList.add('translate-fade');
      
      setTimeout(() => {
        const key = element.dataset.translate;
        if (translations[key]) {
          if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
            element.placeholder = translations[key];
          } else {
            element.textContent = translations[key];
          }
        }
        
        element.classList.remove('translate-fade');
        element.classList.add('translate-complete');
        
        setTimeout(() => {
          element.classList.remove('translate-complete');
        }, 300);
      }, 150);
    });
    
    // Handle placeholder translations
    const placeholderElements = document.querySelectorAll('[data-translate-placeholder]');
    placeholderElements.forEach(element => {
      const key = element.dataset.translatePlaceholder;
      if (translations[key]) {
        element.placeholder = translations[key];
      }
    });
  }
  
  updateLanguageDisplay() {
    const currentLangSpan = document.getElementById('currentLanguage');
    if (!currentLangSpan) return;
    
    const langMap = {
      'en': 'EN',
      'hi': '‡§π‡§ø'
    };
    
    currentLangSpan.textContent = langMap[this.currentLanguage] || 'EN';
    
    // Update active state in dropdown
    document.querySelectorAll('.language-option').forEach(option => {
      option.classList.remove('active');
      if (option.dataset.lang === this.currentLanguage) {
        option.classList.add('active');
      }
    });
  }
  
  getCurrentLanguage() {
    return this.currentLanguage;
  }
  
  getTranslation(key) {
    return this.translations[this.currentLanguage][key] || this.translations['en'][key] || key;
  }
  
  translateElement(element, key) {
    const translation = this.getTranslation(key);
    if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
      element.placeholder = translation;
    } else {
      element.textContent = translation;
    }
  }
